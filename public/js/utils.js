function zeichneGraph(){   
    var spotSize = 50;
    var lineSize = 5;
    
    var clipboard = document.getElementById('myCanvas');

    // clear clipboard!
    clipboard.width+=0;
    
    var experimentName = getExperimentName();
    if(amountOfLines<=clicks){
        scoreText += "Versuch: "+experimentName+ ", getroffen=" +clicks+", verfehlt="+fails + ", Zeit=not implemented"+"<br>";
        // score ausgeben
        document.getElementById("score").innerHTML = scoreText;
        
        refreshCounters();
        return;
    }
    
    var generator = new Generator(generatorType, amountOfLines);
    var lineList = generator.getLines();
    var line = lineList[clicks];

    
    // calculate points of line and middle spot
    var x1 = line.getX1();
    var y1 = line.getY1();
    var x2 = line.getX2();
    var y2 = line.getY2();
    var m1 = (x1+x2)/2;
    var m2 = (y1+y2)/2;
    
    var ctx = clipboard.getContext('2d');
    var canvasOffset = $("#myCanvas").offset();
    var offsetX = canvasOffset.left;
    var offsetY = canvasOffset.top;

    ctx.beginPath();
    ctx.moveTo(x1,y1);
    ctx.lineTo(x2, y2);
    ctx.lineWidth = lineSize;
    ctx.strokeStyle = '#ffffff';
    ctx.stroke();
    
    //alert('1');
    // jetzt das click-element
    var elem = document.getElementById('myCanvas');
    var elemLeft = elem.offsetLeft;
    var elemTop = elem.offsetTop;
    var context = elem.getContext('2d');
    var elements = [];
    //alert('2');
    
    // Add element to listen.
    elements.push({
        colour: '#ffffff',
        width: spotSize+20,
        height: spotSize,
        top: m2-(spotSize/2),
        left: m1-(spotSize/2)-10
     });
    //alert('6');
    // Render elements.
    elements.forEach(function(element) {
        //alert('element');
        //context.fillStyle = element.colour;
        if(isMiddleVisible){
            context.fillStyle = 'rgba(0, 0, 121, 0.4)';
        }
        else{
            context.fillStyle = 'rgba(0, 0, 0, 0.0)';
        }
        context.fillRect(element.left, element.top, element.width, element.height);
     });
    
    elem.addEventListener('click', function(event) {
        var x = event.pageX - elemLeft,
        y = event.pageY - elemTop;
        // Collision detection between clicked offset and element.
        elements.forEach(function(element) {
            if (y > element.top && y < element.top + element.height
            && x > element.left && x < element.left + element.width) {
                //alert('clicked an element');
                removeOldListenerByName(elem, 'click');
                elements = [];
                countHit();
                playSoundByID('goodaudio');
                zeichneGraph();
                if(needToPrint){
                    printToHTML();
                }
            }
            else{
                countMiss();
                playSoundByID('wrongaudio');
            }
        });
    }, false);
}

